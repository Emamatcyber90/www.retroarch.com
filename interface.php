        <h2>Overview</h2>

        <div class="row">
            <div class="col-md-6">
                <p><img src="images/xmb-tabs.png"></p>
            </div>
            <div class="col-md-6">
                <p>When you launch RetroArch for the first time, the interface looks like this. There are 8&nbsp;tabs:</p>
                <ul>
                    <li><b>Main Menu:</b> perform important actions like launching a game or quitting the program.</li>
                    <li><b>Settings:</b> to configure RetroArch</li>
                    <li><b>History:</b> your games history list</li>
                    <li><b>Images:</b> your images history list</li>
                    <li><b>Music:</b> your music history list</li>
                    <li><b>Videos:</b> you videos history list</li>
                    <li><b>Netplay Rooms:</b> find game sessions hosted by other gamers and join them.</li>
                    <li><b>Add:</b> this tab lets you scan your game collection to generate per system playlists. Playlists will be added as new tabs on the&nbsp;right.</li>
                </ul>
            </div>
        </div>

        <h2>Integrated downloads</h2>

        <div class="row">
            <div class="col-md-6">
                <p>In <b>Main Menu->Online Updater</b> you can download and update all the required or optional components for RetroArch.</p>
                <p>This&nbsp;includes:</p>
                <ul>
                    <li><b>Libretro cores:</b> emulators and game engines.</li>
                    <li><b>A game database:</b> used by our scanning system to build playlists and display the games metadatas.</li>
                    <li><b>Game thumbnails:</b> boxart, title screen, in game screen, for each system.</li>
                    <li><b>Joypad profiles:</b> default mapping for common joypads to work out of the box. <a href="http://retroarch.com/index.php?page=joypad-autoconfig">Read more</a></li>
                    <li><b>Cheats:</b> cheats for the most common games.</li>
                    <li><b>Shaders:</b> video filters to apply on the game screen. <a href="http://retroarch.com/index.php?page=shaders">Read more</a></li>
                    <li>A few free games and&nbsp;homebrews.</li>
                </ul>
            </div>
            <div class="col-md-6">
                <p><img src="images/xmb-updater.png"></p>
            </div>
        </div>

        <h2>Fully joypad driven</h2>

        <div class="row">
            <div class="col-md-6">
                <p><img src="images/xmb-osk.png"></p>
            </div>
            <div class="col-md-6">
                <p>In RetroArch XMB, <b>the joypad is the preferred way to navigate</b>. You can perform every action using the pad, like launching games or triggering the menu from the active&nbsp;game.</p>

                <p>When you need to type text in the settings interface, a multilanguage <b>On Screen Keyboard</b> will be displayed to let you type words and&nbsp;passwords.</p>

                <p>The menu has been designed as an immersive experience: you will not have to switch from your joypad to your keyboard to perform actions using the menu while your game keeps playing in the&nbsp;background.</p>
            </div>
        </div>

        <h2>Playlists</h2>

        <p>You can add new tabs to XMB by scanning your game collection and creating playlists. Games will be sorted per system. You can also write playlists manually to display a list of your favorite games, or any list you can think&nbsp;of.</p>

        <p>Playlist entries can be associated to a database entry to display the metadata of the game: Release Year, Genre, Developer, Number of Players,&nbsp;etc.</p>

